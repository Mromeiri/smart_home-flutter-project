class TemperatureProvider with ChangeNotifier {
  Temperature temp = Temperature(temperature: 0, humidity: 0);

  Temperature get temperature => temp;

  void setTemperature(Temperature _newTemperature) {
    temp = _newTemperature;
    notifyListeners();
  }
}
